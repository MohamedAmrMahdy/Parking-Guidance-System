    void Addrows(){
        jTable1.setRowHeight(30);
        ArrayList<Entry_Empolyee> ale = new ArrayList<>();
        ale = (ArrayList<Entry_Empolyee>) FM.read("EntryEmployee.bin");
        ArrayList<Exit_Empolyee> alx = new ArrayList<>();
        alx = (ArrayList<Exit_Empolyee>) FM.read("ExitEmployee.bin");
        Object[] row = new Object[7];
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for(int i=0;i<ale.size();i++){
            row[0]=ale.get(i).getID();
            row[1]=ale.get(i).getName();
            row[2]=ale.get(i).getUserName();
            row[3]=ale.get(i).getPassword();
            row[4]=ale.get(i).getShift();
            row[5]=ale.get(i).getHoursNo();
            row[6]="Entry EMP.";
            model.addRow(row);
        }
        for(int i=0;i<alx.size();i++){
            row[0]=alx.get(i).getID();
            row[1]=alx.get(i).getName();
            row[2]=alx.get(i).getUserName();
            row[3]=alx.get(i).getPassword();
            row[4]=alx.get(i).getShift();
            row[5]=alx.get(i).getHoursNo();
            row[6]="Exit EMP.";
            model.addRow(row);
        }
    }

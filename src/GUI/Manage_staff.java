    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        int row = jTable1.getSelectedRow();
        int id = (int) jTable1.getModel().getValueAt(row, 0);
        String Role = (String) jTable1.getModel().getValueAt(row, 6);
        if(Role.equals("Entry EMP.")){
            EntryEmployees = (ArrayList<Entry_Empolyee>) FM.read("EntryEmployee.bin");
            EntryEmployees.remove(id);
            FM.write("EntryEmployee.bin", EntryEmployees);
        }
        if(Role.equals("Exit EMP.")){
            ExitEmployees = (ArrayList<Exit_Empolyee>) FM.read("ExitEmployee.bin");
            ExitEmployees.remove(id);
            FM.write("ExitEmployee.bin", ExitEmployees);
        }
        model.removeRow(jTable1.getSelectedRow());
    }//GEN-LAST:event_jButton1MouseClicked

